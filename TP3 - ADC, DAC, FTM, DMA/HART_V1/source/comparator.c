static bool initialized = false;

/*******************************************************************************
 *******************************************************************************
                        GLOBAL FUNCTION DEFINITIONS
 *******************************************************************************
 ******************************************************************************/

void initComparator(comparator_cfg_t config){

    initialized = true;

    if(config.histEnabled){
        CMP0->CR0 |= CMP_CR0_HYSTCTR(1); //Sera necesario cambiar el level?
    }

    CMP0->MUXCR |= CMP_MUXCR_MSEL(0); //Select reference minus value
    CMP0->CR1 |= CMP_CR1_COS(1); //Disable filter

    CMP0->CR1 |= CMP_CR1_EN(1); //Enable comparator
}


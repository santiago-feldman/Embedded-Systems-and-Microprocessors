void initNCO(void)
{
	idNCO = createTimer(39.0625, NCO_PISR); // Freq=25600Hz
}

void setM(int M_dds)
{
	if (M_dds > 0 && M_dds < MAX_M)
	{
		M = M_dds;
	}
}

void stopNCO(void)
{
	stopTimer(idNCO);
}

void startNCO(void)
{
	startTimer(idNCO);
}

void NCO_PISR(void)
{
	static DACData_t phaseRegister = 0;
	DAC_SetData(DAC0, sineLookUpTable[phaseRegister]);
	phaseRegister += M;
	phaseRegister %= LEN(sineLookUpTable);
}


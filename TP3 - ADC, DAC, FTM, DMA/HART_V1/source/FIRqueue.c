bool putFIR(FIRqueue_t *queue, FIRqueuedata_t data)
{
    queue->front = (queue->front >= FIR_ORDER - 1) ? 0 : queue->front + 1; // Corro la cabeza para apuntar al siguiente dato
    queue->buffer[queue->front] = data;                                    // Coloco el dato
    return true;
}

FIRqueuedata_t readValueFIR(FIRqueue_t *queue, uint16_t tap)
{
    uint16_t index = queue->front > tap ? (queue->front - tap) : (FIR_ORDER - (tap - queue->front));
    return queue->buffer[index];
}

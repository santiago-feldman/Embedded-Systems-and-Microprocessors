/* Función que se llama 1 vez, al comienzo del programa */
void App_Init(void)
{
  bool success = initTimer();
  initCardReader();
  success &= InitEncoder();
  display_init();
  uartInit(0, config);

  led_init(OFF, OFF, OFF); // Ordenados por D1 - D2 - D3
}

/* Función que se llama constantemente en un ciclo infinito */
void App_Run(void *pArg)
{
  App_Init();

  while (1)
  {
    FSM();
  }
}

